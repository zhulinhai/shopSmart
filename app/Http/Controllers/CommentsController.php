     * @return \Illuminate\Http\Response
     */
    public function create()
    {
        $all = Article::all('id');
        $articles = array();
        foreach ($all as $item) {
            $articles[] =$item->id;
        }
        return view('admin.comments.create', ['articles'=>$articles]);
    }

